fun main() {
    // Atribuição de stringas nas variáveis.
	val primeiroNome = "Daniel"
    val ultimoNome = "Ohata"
    // Concatena as duas strings sem espaçamento entre elas
    val nomeCompleto = primeiroNome + ultimoNome
    println(nomeCompleto)
    
	// Atribuição de valores inteiros às variáveis
    val n1 = 9
    val n2 = 5
	// A variável média irá receber a soma dos dois valores
	// divido por dois
    val media = (n1 + n2)/2
    println (media)
}