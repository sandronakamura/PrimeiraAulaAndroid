
fun main() {
    // Atribuição de um inteiro a variável
	val notaFinal = 7
	// Condição para verificar se a nota é maior ou igual a 7
	// caso seja ira exibir a mensagem "APROVADO" caso seja menor
	// que 7 sera exibido "REPROVADO"
    if (notaFinal >= 7){
        println("APROVADO")
    } else{
        println("REPROVADO")
    }
}